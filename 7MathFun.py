import math

pi = 3.14
x=13
y=50
z=8

print(round(pi))        #returns 3
print(math.ceil(pi))    #returns 4  next value
print(math.floor(pi))   #returns 3  back value
print(abs(pi))          #absolute value 3.14
print(pow(pi,2))        #power of value
print(math.sqrt(196))   #square root
print(max(x,y,z))       #largest
print(min(x,y,z))       #smallest