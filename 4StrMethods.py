name = 'Joyal Joy'

#Length of the string
print(len(name))

#Find the Index Value
print(name.find("a"))

#Capitaplize Only the First letter
print(name.capitalize())

#uppaercase and lowercase
print(name.upper())
print(name.lower())

#check is a digit
print(name.isdigit())

#check is albhabets only(not special or other char)
print(name.isalpha())

#count
print(name.count('o'))

#replace the char
print(name.replace('o','u'))

#print multiple times
print(name*8)